#include <iostream>
using namespace std;
int main()
{
    float a;
    int b;
    short c;
    long d;
    cout << "enter  a: " <<endl;
    cin >> a; 
    cout << "enter  b: " <<endl;
    cin >> b; 
    cout << "enter  c: " <<endl;
    cin >> c; 
    cout << "enter  d: " <<endl;
    cin >> d; 
    cout <<  a    << " - "<< b << " = " << a-b << endl ;
    cout <<  b   << " * "<< c << " = " << b*c << endl ;
    cout <<  c  << " + "<< d << " = " << c+d  << endl ;
    cout <<  a  << " / "<< d << " = " << a/d << endl ;
 
    return 0;
}
